                            Dari alam untuk alam. Istilah ini cocok digunakan untuk kemasan tradisonal. Kemasan sudah ada sejak jaman dahulu kala. Nenek moyang kita membungkus makanan menggunakan bahan yang berasal dari alam. Misalkan mengemas pisang dengan anyaman bambu, membungkus nasi dengan daun pisang disebut lontong, daging sapi dibungkus dengan daun jati, dan masih banyak lagi. </br></br>
							Dengan perkembangan pola hidup masyarakat sekarang berubah, maka kemasan berubah lebih modern. Peningkatan industri dan kemajuan ilmu pengetahuan dan teknologi (IPTEK), juga menjadikan faktor perkembangan kemasan makanan. Fungsinya pun sekarang lebih beragam. </br></br>
								Kemasan adalah wadah atau tempat yang digunakan untuk mengemas suatu produk yang dilengkapi dengan tulisan, label, keterangan lain yang menjelaskan isi, kegunaan dan informasi lain yang perlu disampiakn kepada konsumen. </br></br>
							Fungsi kemasan: melindungi produk dari benturan ketika pengiriman; melindungi produk dari kerusakan fisik, kimia, dan biologis; Mencegah terjadinya kontaminasi bahan dari luar; Menjaga mutu selama penyimpanan; Sebagai pengawet makanan; Memenuhi standart mutu produk; Mempermudah pemakaian, penyimpanan, pendistribusian; Menampilkan identitas dan informasi produk, sehingga dapat meningkatkan daya tarik konsumen, peningkatan keuntungan, dan peningkatan daya saing. </br></br>
							Dari segala kelebihan yang dimiliki kemasan, ada juga kelemahan dan kekurangannya. Yaitu pencemaran lingkungan yang disebabkan penggunaan nahan kemasan sintetik yang tidak ramah lingkungan; Harga kemasan yang lebih mahal dibanding dengan produk yang dikemas, sehingga harga jual produk tinggi; Kurang ekonomis dikarenakan sekali pakai kemudian dibuang; Tidak semua kemasan dilengkapi dengan informasi label yang jelas. Label yang baik harus memuat unsur-unsur : Nama bahan, nomor pendaftaran produk, komposisi bahan, kode produksi, berat/volume produk, aturan pemakaian, tanggal kadaluarsa, peringatan bahaya sampingan/ kontra indikasi, saran penyajian. </br></br>
                        Bahan dan Bentuk Kemasan </br>
•	Bentuk Kemasan</br>
&emsp; Kemasan kaku (rigid), misal : logam, kayu, karton, gelas
Kemasan semi kaku, misal: alumunium, folding karton, set up box
Kemasan fleksibel, misal: kertas, plastik, alumunium foil </br>

•	Bahan Kemasan</br>
&emsp;1.	Kayu</br>
&emsp;Pengemasan untuk produk yang memiliki bobot besar dan beresiko tinggi untuk rusak. Untuk pengemasan produk kecil tidak efektif. Apabila ditumpuk sangat kuat. Untuk distribusi juga baik. Namun kurang kedap air. Kemasan kayu juga membutuhkan ruang penyimpanan besar. Contoh: kayu utuk krat, kayu untuk pallet. </br>

&emsp;2.	Logam</br>
&emsp;Kemasan logam memiliki kekuatan mekanis yang baik. Jenis logam yang biasa digunakan untuk kemasan adalah baja dan kaleng logam. Baja digunakan untuk pengemasan produk cair, semi padat, dan tepung. Contoh minyak saus, manisan buah yang dikalengkan dan pelarut organik. Kaleng logam terbuat dari plat baja yang dilapisi timah dikedua sisinya. Contoh kaleng minuman soda. </br>

&emsp;3.	Gelas</br>
&emsp;Gelas telah sangat lama digunakan untuk kemasan produk. Gelas terbuat dari pasir, soda abu, kapur/campuran alkali. 
Kelebihan gelas adalah bersifat transparan, tidak beraksi dengan produk, kedap terhadap gas air dan bau, keawetan aroma, rasa dan warna produk, dapat dibentuk dan didesain, mudah diwarnai, tahan sugu rendah dan sterilisasi, dapat divakum.
Kelemahannya yaitu bersifat rapuh dan mudah pecah.
Contoh : Produk minuman, saos, obat, bahan kimia, dan kosmetik </br>

&emsp;4.	Kertas</br>
&emsp;Kertas adalah bahan tipis yang dihasilkan dari proses pengepresan serat kayu yang berasal dari pulp. Kelebihan kemasan kertas adalah mudah dilipat ketika disimpan, mudah diwarnai (dicetak), harga relatif murah. Kekurangannya yaitu sekali pakai langsung dibuang, mudah rusak terkena air.

